namespace TwitchGQL.Models.Enums
{
    /// <summary>
    /// Enumerates valid badge image sizes.
    /// </summary>
    public enum BadgeImageSize
    {
        /// <summary>
        /// The base badge image size: 18x18.
        /// </summary>
